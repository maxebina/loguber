                </p>
                <p>
                    No Brasil, o principal modal de transporte, responsável por 76% da circulação de produtos e insumos, é o rodoviário, por alcançar, com facilidade, regiões onde diferentes opções, como aéreo ou aquaviário, não alcançam.
                </p>
                <p>
                    Porém, apesar de ser a principal forma de transporte, será que é o mais adequado para todos os tipos de carga?
                </p>
                <p>
                    Com certeza, não, uma vez que há fatores como distância, vida útil da carga – no caso de perecíveis – tamanho e peso, que fazem com que outros modais sejam mais adequados.
                </p>
                <p>
                    Então vamos ver quais os principais tipos de carga são os mais adequados para o transporte rodoviário.
                </p>
                <h3>
                    Principais tipos de carga para modal rodoviário
                </h3>
                <ul>
                    <li>
                        <strong>Cargas secas:</strong> São produtos não perecíveis, geralmente industrializados, que podem ser transportados em quaisquer horários e condições climáticas, sem que a carga seja danificada.Alguns dos principais tipos de cargas secas são:materiais de construção, equipamentos, madeiras, ferragens, produtos de limpeza, beleza, mobiliários, etc.
                    </li>
                    <li>
                        <strong>Cargas perigosas:</strong> São todo e qualquer produto que apresente risco às pessoas, instalações e meio ambiente, sejam inflamáveis, explosivas, oxidantes, venenosas, infecciosas, poluentes, radioativas, ou corrosivas.Por isso, este tipo de carga deve ser transportado com uma série de cuidados, como identificação sobre a carga, empacotamento e acondicionamento, corretos, além de documentação correta e em dia.
                    </li>
                    <li>
                        <strong>Cargas frigoríficas:</strong> Um dos tipos de carga que necessita de maior cuidado no transporte, inclusive o cumprimento, por parte das transportadoras, das normas e exigências para que estejam autorizadas a realizar este tipo de transporte.As cargas frigoríficas são, basicamente, compostas por itens de alimentação que devem ser acondicionados de forma a não sofrerem deterioração por temperatura, ou mesmo por falta de higiene dos caminhões.
                    </li>
                    <li>
                        <strong>Cargas graneleiras:</strong> Este é o tipo de carga líquida ou sólida que não necessita ser acondicionada de forma específica, ou seja, não há necessidade de embalagens ou invólucros especiais.Apesar de não necessitarem ser acondicionadas de forma específica, devem ser transportadas individualmente, para que não se misturem e percam suas características originais, assim como não devem, em hipótese alguma, ser transportadas sem proteção.
                    </li>
                    <li>
                        <strong>Cargas Vivas:</strong> Esta é a uma das modalidades mais delicadas de carga, pois consiste de animais vivos, necessitando de alto nível de especialização, uma vez que deve evitar o sofrimento dos animais.Este tipo de transporte deve ter espaço adequado para que o animal possa ficar de pé em posição natural, o caminhão deve ser livre de outros tipos de carga ou peças que possam machucar o animal, além de indicar que sua carga é viva e a posição em que se encontram.
                    </li>
                    <li>
                        <strong>Carga veicular:</strong> Este tipo de carga é feito por caminhões cegonha ou guinchos e transportam, obviamente, veículos de todos os tipos e merecem um cuidado especial para que não se desprendam ou desbalaceiem o caminhão que os transporta, podendo causar sérios acidentes.
                    </li>
                    <li>
                        <strong>Carga em pallets ou racks:</strong> Esta é uma modalidade de carga que pode levar quase qualquer produto, mas se destaca por facilitar a sustentação e acondicionamento de diferentes tipos de produtos, assim como seu manuseio na carga e descarga de caminhões que, geralmente, apresentam abertura lateral.
                    </li>
                </ul>
